package projIfElse;

public class DataTypes {
	
	public static void main (String[] args) {
		//byte
		byte age;
		age=45;
		System.out.println("age is " + age);
		
		//short
		short studentID;
		studentID=17952;
		System.out.println("Student id is " + studentID);
		
		//int
		int populationofUSA;
		populationofUSA=330000000;
		System.out.println("USA population is " + populationofUSA);
		
		//long
		long distanceFromMoon;
		distanceFromMoon=472839423842384234L;
		System.out.println("Distance from moon is " + distanceFromMoon);
		
		//float
		float salary;
		salary=12099.89F;
		System.out.println("Salary is " + salary);
		
		//double
		double budget;
		budget= 46546456456.45;
		System.out.println("Budget is " + budget);
		
		//boolean
		boolean isMarried;
		isMarried = false;
		System.out.println("Is Married " + isMarried);
		
		//char
		char gender;
		gender = 'M';
		System.out.println("Gender is " + gender);
	}

}
